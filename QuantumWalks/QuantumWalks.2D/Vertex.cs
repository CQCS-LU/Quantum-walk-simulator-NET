
namespace CQCS.QuantumWalks.Grid2D
{
    /// <summary>
    /// Represents a vertex of 2D grid.
    /// </summary>
    public struct Vertex
    {
        public int X;
        public int Y;

        public Vertex(int x, int y)
        {
            X = x;
            Y = y;
        }
    }
}
